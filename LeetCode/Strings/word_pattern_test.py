from word_pattern import wordPattern

tests = [
    {"query": ["abba", "dog cat cat dog"], "output": True},
    {"query": ["abba", "dog cat cat fish"], "output": False},
    {"query": ["aaaa", "dog cat cat dog"], "output": False},
]

passed_cases = 0
failed_cases = 0
total_cases = len(tests)

for i, test in enumerate(tests):
    print(f"Test Case {i + 1}")
    print(f"Expected Output:", test["output"])
    result = wordPattern(test["query"][0], test["query"][1])
    print(f"Actual Output:", result)

    if result == test["output"]:
        print("------ PASSED ------ \n")
        passed_cases += 1
    else:
        print("------ FAILED ------ \n")
        failed_cases += 1

print("*" * 100, "\n")
print(f"Result: {passed_cases}/{total_cases} \n")
