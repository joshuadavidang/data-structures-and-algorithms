from reverse_words import reverseWords

tests = [
    {"query": "the sky is blue", "output": "blue is sky the"},
    {"query": "  hello world  ", "output": "world hello"},
    {"query": "a good   example", "output": "example good a"},
]

passed_cases = 0
failed_cases = 0
total_cases = len(tests)

for i, test in enumerate(tests):
    print(f"Test Case {i + 1}")
    print(f"Expected Output:", test["output"])
    result = reverseWords(test["query"])
    print(f"Actual Output:", result)

    if result == test["output"]:
        print("------ PASSED ------ \n")
        passed_cases += 1
    else:
        print("------ FAILED ------ \n")
        failed_cases += 1

print("*" * 100, "\n")
print(f"Result: {passed_cases}/{total_cases} \n")
