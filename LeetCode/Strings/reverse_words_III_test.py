from reverse_words_III import reverseWords

tests = [
    {"query": "Let's take LeetCode contest", "output": "s'teL ekat edoCteeL tsetnoc"},
    {"query": "God Ding", "output": "doG gniD"},
]

passed_cases = 0
failed_cases = 0
total_cases = len(tests)

for i, test in enumerate(tests):
    print(f"Test Case {i + 1}")
    print(f"Expected Output:", test["output"])
    result = reverseWords(test["query"])
    print(f"Actual Output:", result)

    if result == test["output"]:
        print("------ PASSED ------ \n")
        passed_cases += 1
    else:
        print("------ FAILED ------ \n")
        failed_cases += 1

print("*" * 100, "\n")
print(f"Result: {passed_cases}/{total_cases} \n")
